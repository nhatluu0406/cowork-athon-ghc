/** Write `<configDir>/plugin/ms365.ts`. Refuses if the (static) source unexpectedly contains `forbidden`. */
export function writeMs365Plugin(configDir: string, forbidden?: string): void {
  if (forbidden !== undefined && forbidden.length > 0 && MS365_PLUGIN_SOURCE.includes(forbidden)) {
    throw new Error("ms365-plugin-file: refusing to write plugin containing a secret value");
  }
  const target = join(configDir, "plugin", "ms365.ts");
  mkdirSync(dirname(target), { recursive: true });
  writeFileSync(target, MS365_PLUGIN_SOURCE, "utf8");
}

/**
 * Copy `@opencode-ai/plugin` (+ its declared `dependencies`, recursively) from `nodeModulesRoot`
 * into `<configDir>/node_modules` so the plugin's import resolves offline — independent of
 * OpenCode's own background `npm install` (which needs network). A missing source package only
 * logs a warning (via the caller-redacted `log` sink) and is skipped; OpenCode's own install
 * remains the fallback.
 */
export function seedMs365PluginDeps(
  configDir: string,
  nodeModulesRoot: string,
  log: (m: string) => void,
): void {
  const targetRoot = join(configDir, "node_modules");
  const copied = new Set<string>();
  const copyPkg = (name: string): void => {
    if (copied.has(name)) return;
    copied.add(name);
    const src = join(nodeModulesRoot, ...name.split("/"));
    if (!existsSync(src)) {
      log(`ms365_plugin_seed_missing pkg=${name}`);
      return;
    }
    try {
      cpSync(src, join(targetRoot, ...name.split("/")), { recursive: true });
    } catch {
      log(`ms365_plugin_seed_copy_failed pkg=${name}`);
      return;
    }
    try {
      const pkg = JSON.parse(readFileSync(join(src, "package.json"), "utf8")) as {
        dependencies?: Record<string, string>;
      };
      for (const dep of Object.keys(pkg.dependencies ?? {})) copyPkg(dep);
    } catch {
      log(`ms365_plugin_seed_pkgjson_unreadable pkg=${name}`);
    }
  };
  copyPkg("@opencode-ai/plugin");
}
